public class Main {
    public static void main(String[] args) {
        System.out.println("Hello World");

        // Declaration of  variable x
        int x;

        //Initialization of variable x
        x = 15;

        /*
        This is
        multi line
        comment


         */
        System.out.println(x);

        //Initialization and Declaration at the same line
        int y = 10;
        System.out.println("y = " + y);
        //String data type

        String name = "Karlis";
        System.out.println(name);

        //Double data type
        double temperature = 36.6d + 1;
        System.out.println(temperature);

        String temperature2 = "36.6" + 1;
        System.out.println(temperature2);

        //Final variable

        final double pie;
        pie = 3.14156647638767d;
    }

}
